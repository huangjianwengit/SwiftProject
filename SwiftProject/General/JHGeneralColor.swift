import UIKit

class JHGeneralColor: NSObject {
    
    class func themeColor() -> UIColor {
        return UIColor.init("#EE1B2B")
    }
    
    class func themeBackColor() -> UIColor {
        return UIColor.init(246, 246, 246)
    }
    
    class func navigationBarColor() -> UIColor {
        
        return UIColor.init("#EE1B2B")
    }
    
    class func navigationBarTitleColor() -> UIColor {
        return UIColor.black
    }
    
    class func tabBarTitleNormalColor() -> UIColor {
        return UIColor.black
    }
    class func tabBarTitleSelectedColor() -> UIColor {
        return UIColor.init("#EE1B2B")
    }

}
